    private void btTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTimActionPerformed
        DefaultTableModel model = (DefaultTableModel) TbDiem.getModel();

        model.setRowCount(0);

        try {
            Connection con = null;
            con = SQLServerConnection.getSQLServerConnection();

            String sql = "SELECT * FROM  DIEMSO JOIN SINHVIEN ON DIEMSO.MASV = SINHVIEN.MASV WHERE MAMH = ?";
            SQLServerPreparedStatement ps = (SQLServerPreparedStatement) con.prepareStatement(sql);
            ps.setString(1, getMaMH());
            ResultSet rs = ps.executeQuery();

            String row[] = new String[7];
            while (rs.next()) {
                row[0] = rs.getString("MASV");
                row[1] = rs.getString("TENSV");
                row[2] = rs.getString("DIEMQT");
                row[3] = rs.getString("DIEMTH");
                row[4] = rs.getString("DIEMGK");
                row[5] = rs.getString("DIEMCK");
                row[6] = rs.getString("DIEMTB");
                model.addRow(row);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btTimActionPerformed

    private void TbDiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TbDiemMouseClicked
        DefaultTableModel model = (DefaultTableModel) TbDiem.getModel();
        int indexTB = TbDiem.getSelectedRow();
        if (indexTB < model.getRowCount() && indexTB >= 0) {
            mssvBox.setText(model.getValueAt(indexTB, 0).toString());
            hotenBox.setText(model.getValueAt(indexTB, 1).toString());
            QT.setText(model.getValueAt(indexTB, 2).toString());
            TH.setText(model.getValueAt(indexTB, 3).toString());
            GK.setText(model.getValueAt(indexTB, 4).toString());
            CK.setText(model.getValueAt(indexTB, 5).toString());
            TB.setText(model.getValueAt(indexTB, 6).toString());
        }
    }//GEN-LAST:event_TbDiemMouseClicked

    private void btLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLuuActionPerformed
        DefaultTableModel model = (DefaultTableModel) TbDiem.getModel();
        int indexTB = TbDiem.getSelectedRow();

        try {
            double qt = Double.parseDouble(QT.getText());
            double th = Double.parseDouble(TH.getText());
            double gk = Double.parseDouble(GK.getText());
            double ck = Double.parseDouble(CK.getText());
            double tb = Double.parseDouble(TB.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Điểm phải là 1 số dương!");
        }

        double qt = Double.parseDouble(QT.getText());
        double th = Double.parseDouble(TH.getText());
        double gk = Double.parseDouble(GK.getText());
        double ck = Double.parseDouble(CK.getText());
        double tb = Double.parseDouble(TB.getText());

        if (qt < 0 || th < 0 || gk < 0 || ck < 0 || tb < 0) {
            JOptionPane.showMessageDialog(this, "Điểm không được âm!");
        } else if (QT.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập điểm QT!");
        } else if (TH.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập điểm TH!");
        } else if (GK.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập điểm GK!");
        } else if (CK.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập điểm CK!");
        } else if (TB.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập điểm TB!");
        } else {

            try {
                String strSQL = "UPDATE DIEMSO SET DIEMQT = ?, DIEMTH = ?, DIEMGK = ?, DIEMCK = ?, DIEMTB=? WHERE MAMH = ? AND MASV = ?";
                Connection con = null;
                con = SQLServerConnection.getSQLServerConnection();
                SQLServerPreparedStatement ps = (SQLServerPreparedStatement) con.prepareStatement(strSQL);

                ps.setString(1, QT.getText());
                ps.setString(2, TH.getText());
                ps.setString(3, GK.getText());
                ps.setString(4, CK.getText());
                ps.setString(5, TB.getText());
                ps.setString(6, getMaMH());
                ps.setString(7, mssvBox.getText());

                if (indexTB < model.getRowCount() && indexTB >= 0) {
                    model.setValueAt(TH.getText(), indexTB, 2);
                    model.setValueAt(QT.getText(), indexTB, 3);
                    model.setValueAt(GK.getText(), indexTB, 4);
                    model.setValueAt(CK.getText(), indexTB, 5);
                    model.setValueAt(TB.getText(), indexTB, 6);
                }

                int n = ps.executeUpdate();

                if (n != 0) {
                    JOptionPane.showMessageDialog(this, "Lưu điểm thành công!");
                } else {
                    JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi!", "Error", JOptionPane.ERROR_MESSAGE);
                }

            } catch (Exception e) {
                System.err.println(e);
            }
        }
    }//GEN-LAST:event_btLuuActionPerformed

